<h3>  <b>Age </b> <br>
You are aged between 18 and 65.

In some countries national legislation permits 16-17 years old to donate provided that they fulfil the physical and hematological criteria required and that appropriate consent is obtained.
In some countries, regular donors over the age of 65 may be accepted at the discretion of the responsible physician. The upper age limit in some countries are 60. 
<br> <b> Weight </b> <br> 
You weigh at least 50 kg.    

